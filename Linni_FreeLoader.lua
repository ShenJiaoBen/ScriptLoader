local Players = game:GetService("Players")
local MarketplaceService = game:GetService("MarketplaceService")
local LocalPlayer = Players.LocalPlayer

local function IsInList(target, list)
    for _, id in ipairs(list) do
        if target == id then return true end
    end
    return false
end

local _creatorCache = nil
local function GetCreatorId()
    if _creatorCache then return _creatorCache end
    
    local success, info = pcall(function()
        return MarketplaceService:GetProductInfo(game.PlaceId).Creator.CreatorTargetId
    end)
    
    if success then
        _creatorCache = info
        return info
    end
    return nil
end
    
local function ValidateGroup()
    local success, result = pcall(function()
        return LocalPlayer:IsInGroup(Config.GroupID)
    end)

    if not success or not result then
        if setclipboard then
            setclipboard(Config.GroupInfo)
        end
        LocalPlayer:Kick(Config.KickMessage)
        return false
    end
    return true
end
local function LoadTargetScript(url, vars)
    if vars then
        for k, v in pairs(vars) do
            if getgenv then 
                getgenv()[k] = v 
            end
            _G[k] = v
        end
    end
    local success, err = pcall(function()
        loadstring(game:HttpGet(url))()
    end)

    if not success then
        warn("[LinniLoader] 脚本加载失败: " .. tostring(err))
    else
        print("[LinniLoader] 脚本加载成功: " .. url)
    end
end
